def somaDoisValores(a,b):
	return a + b

print("Olá pessoal, vamos somar?")

num1 = int(input('Valor do primeiro número: '))
num2 = int(input('Valor do segundo número: '))

print(somaDoisValores(num1, num2))

def subtracaoDoisValores(a,b):
	return a - b

print("Olá pessoal, vamos subtrair?")

num1 = int(input('Valor do primeiro número: '))
num2 = int(input('Valor do segundo número: '))

print(subtracaoDoisValores(num1, num2))
